public class Salary {
    private String name;
    private int hourly;
    private Double rate;

    public void setName(String name) {
        this.name = name;
    }

    public void setHourly(int hourly) {
        this.hourly = hourly;
    }

    public void setRate(Double rate) {
        this.rate = rate;
    }

    public String getName() {
        return name;
    }

    public int getHourly() {
        return hourly;
    }

    public Double getRate() {
        return rate;
    }
}